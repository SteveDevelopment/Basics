public class Program {
    public static void main(String[] args) {

        // Modulo Operator geht nur bei Ganzzahlen
        //Tut das was übrig bleibt ausgeben

        int number1 = 32/3;             //Geteilt Rechnen
        System.out.println(number1);

        int number2 = 32%3;
        System.out.println(number2);

        int number3 = 31%3;
        int number4 = 30%3;         //lässt sich dadurch teilen
        System.out.println(number3);
        System.out.println(number4);
    }
}
